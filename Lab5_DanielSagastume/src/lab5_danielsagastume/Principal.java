    private void Lista_adminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Lista_adminMouseClicked
        if (evt.isMetaDown()) {
            int row = Arbol_CEO.getClosestRowForLocation(
                    evt.getX(), evt.getY());
            Arbol_CEO.setSelectionRow(row);
            Object v1
                    = Arbol_CEO.getSelectionPath().
                            getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;

            if (nodo_seleccionado.getUserObject() instanceof Admins) {
                persona_seleccionada_admin
                        = (Admins) nodo_seleccionado.
                        getUserObject();
                pop_menu.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            
            }else if(nodo_seleccionado.getUserObject() instanceof Docente){
                persona_seleccionada_docente
                        = (Docente) nodo_seleccionado.getUserObject();
                pop_menu.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }
        }
    }//GEN-LAST:event_Lista_adminMouseClicked

