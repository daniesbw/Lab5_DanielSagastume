    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        int op = Integer.parseInt(JOptionPane.showInputDialog(this, "INGRSE SU CARGO\n"
                + "1. CEO\n"
                + "2. Administrativo\n"
                + "3. Personal de planta\n"
                + "4. Consejo academico\n"
                + "5. Docente\n"
                + "6. Estudiante\n"
                + "7. Tutor"));
        if (op == 1) {
            CEO_SignIn.setModal(true);
            CEO_SignIn.pack();
            CEO_SignIn.setLocationRelativeTo(this);
            CEO_SignIn.setVisible(true);
        } else if (op == 2) {
            Admin_SignIn.setModal(true);
            Admin_SignIn.pack();
            Admin_SignIn.setLocationRelativeTo(this);
            Admin_SignIn.setVisible(true);
        } else if (op == 3) {
            Planta_SignIn.setModal(true);
            Planta_SignIn.pack();
            Planta_SignIn.setLocationRelativeTo(this);
            Planta_SignIn.setVisible(true);
        } else if (op == 4) {
            Consejo_SignIn.setModal(true);
            Consejo_SignIn.pack();
            Consejo_SignIn.setLocationRelativeTo(this);
            Consejo_SignIn.setVisible(true);
        } else if (op == 5) {
            Docente_SignIn.setModal(true);
            Docente_SignIn.pack();
            Docente_SignIn.setLocationRelativeTo(this);
            Docente_SignIn.setVisible(true);
        } else if (op == 6) {
            Estudiante_SignIn.setModal(true);
            Estudiante_SignIn.pack();
            Estudiante_SignIn.setLocationRelativeTo(this);
            Estudiante_SignIn.setVisible(true);
        } else if (op == 7) {
            Tutor_SignIn.setModal(true);
            Tutor_SignIn.pack();
            Tutor_SignIn.setLocationRelativeTo(this);
            Tutor_SignIn.setVisible(true);
        }
        log_tipo = op;
    }//GEN-LAST:event_jButton1MouseClicked

